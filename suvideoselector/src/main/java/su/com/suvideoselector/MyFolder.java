package su.com.suvideoselector;

public class MyFolder {
    String name;
    String path;
    String firstVediopath;
    int num;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPath() {
        return path;
    }

    public void setPath(String path) {
        this.path = path;
    }

    public String getFirstVediopath() {
        return firstVediopath;
    }

    public void setFirstVediopath(String firstVediopath) {
        this.firstVediopath = firstVediopath;
    }

    public int getNum() {
        return num;
    }

    public void setNum(int num) {
        this.num = num;
    }
}
